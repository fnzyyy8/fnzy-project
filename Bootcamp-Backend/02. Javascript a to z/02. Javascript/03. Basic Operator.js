// ! Typeof Basic Operator | Operator, =,+,-,*,/,% | Perbandingan ==,!=, >=, <=

let x = 10;
let y = 10;

// * Operator

// ! Tambah, Kurang, Kali, Bagi, Modulus

let z = x + y;
console.log(z);

z = x - y;
console.log(z);

z = x * y;
console.log(z);

z = x / y;
console.log(z);

z = x % y;
console.log(z);

// * Perbandingan

// ! Sama dengan

z = x == y;
console.log(z);

// ! Tidak sama dengan
z = x !== y;
console.log(z);
